    Map<String, String> demonioFoto = new HashMap<>();

    private void jTable1PropertyChange(java.beans.PropertyChangeEvent evt) {//GEN-FIRST:event_jTable1PropertyChange
        // TODO add your handling code here:
        String nombreDemonio = getValorCelda(jTable1, jTable1.getSelectedRow(), jTable1.getSelectedColumn());
        if (!nombreDemonio.isEmpty() && !demonioActual.equalsIgnoreCase(nombreDemonio)) {
            if (!demonioFoto.containsKey(nombreDemonio)) {
                demonioFoto.put(nombreDemonio, "");
            } else {
                jTable1.setValueAt("", jTable1.getSelectedRow(), jTable1.getSelectedColumn());
            }
        }
    }//GEN-LAST:event_jTable1PropertyChange

    String demonioActual = "";

