    private String getValorCelda(JTable jTable, int fila, int columna) {
        String valor = "";
        if (fila < 0) {
            return valor;
        }
        if (null != jTable.getValueAt(fila, columna)) {
            valor = jTable.getValueAt(fila, columna).toString();
        }
        return valor;
    }

