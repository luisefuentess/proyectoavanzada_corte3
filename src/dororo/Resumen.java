    private void jMenuItem2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem2ActionPerformed
        // TODO add your handling code here:
        String demonio = getValorCelda(jTable1, jTable1.getSelectedRow(), 0);
        if (demonio.isEmpty()) {
            return;
        }
        JFileChooser chooser = new JFileChooser();
        chooser.setFileFilter(new FileNameExtensionFilter("Imágenes (jpg, gif, png, jpeg, svg)", "jpg", "gif", "png", "jpeg", "svg"));
        chooser.showOpenDialog(jPanel1);
        demonioFoto.put(demonio, chooser.getSelectedFile().getAbsolutePath());
    }//GEN-LAST:event_jMenuItem2ActionPerformed

    private void jMenuItem5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem5ActionPerformed
        // TODO add your handling code here:
        String demonio = getValorCelda(jTable1, jTable1.getSelectedRow(), 0);
        if (demonio.isEmpty()) {
            return;
        }
        try {
            String ruta = "";
            if (null == demonioFoto.get(demonio)) {
                JOptionPane.showMessageDialog(jScrollPane1, "No tiene foto / No se encuentra foto", "ERROR", JOptionPane.ERROR_MESSAGE);
                return;
            }
            ruta = demonioFoto.get(demonio).toString();
            Desktop.getDesktop().open(new File(ruta));
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jMenuItem5ActionPerformed

